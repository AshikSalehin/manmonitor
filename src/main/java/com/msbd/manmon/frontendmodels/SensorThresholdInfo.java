
package com.msbd.manmon.frontendmodels;

public class SensorThresholdInfo {
    private double temperatureMin;
    private double temperatureMax;
    private double humidityMin;
    private double humidityMax;
    private double lightMin;
    private double lightMax;
    private double noiseMin;
    private double noiseMax;
    private double waterMin;
    private double waterMax;

    public SensorThresholdInfo() {
    }

    public double getTemperatureMin() {
	return temperatureMin;
    }

    public void setTemperatureMin(double temperatureMin) {
	this.temperatureMin = temperatureMin;
    }

    public double getTemperatureMax() {
	return temperatureMax;
    }

    public void setTemperatureMax(double temperatureMax) {
	this.temperatureMax = temperatureMax;
    }

    public double getHumidityMin() {
	return humidityMin;
    }

    public void setHumidityMin(double humidityMin) {
	this.humidityMin = humidityMin;
    }

    public double getHumidityMax() {
	return humidityMax;
    }

    public void setHumidityMax(double humidityMax) {
	this.humidityMax = humidityMax;
    }

    public double getLightMin() {
	return lightMin;
    }

    public void setLightMin(double lightMin) {
	this.lightMin = lightMin;
    }

    public double getLightMax() {
	return lightMax;
    }

    public void setLightMax(double lightMax) {
	this.lightMax = lightMax;
    }

    public double getNoiseMin() {
	return noiseMin;
    }

    public void setNoiseMin(double noiseMin) {
	this.noiseMin = noiseMin;
    }

    public double getNoiseMax() {
	return noiseMax;
    }

    public void setNoiseMax(double noiseMax) {
	this.noiseMax = noiseMax;
    }

    public double getWaterMin() {
	return waterMin;
    }

    public void setWaterMin(double waterMin) {
	this.waterMin = waterMin;
    }

    public double getWaterMax() {
	return waterMax;
    }

    public void setWaterMax(double waterMax) {
	this.waterMax = waterMax;
    }
    
    
    
}
